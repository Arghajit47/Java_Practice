package com.Arghajit;

import java.util.Scanner;

public class Math {
    public static void main(String[] args) {
        int num1 = 4, num2 = 7;
        System.out.println(java.lang.Math.max(num1, num2));
        System.out.println(java.lang.Math.min(num1, num2));
        System.out.println(java.lang.Math.sqrt(num1));
        System.out.println(java.lang.Math.abs(-6));
        System.out.println(java.lang.Math.random());
        System.out.println(4 + (8-4) * java.lang.Math.random());
    }
}
